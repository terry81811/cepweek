                        <p>我們希望能凝聚每一個人的力量，創造影響力發揮更大的價值，讓部落的這21戶人家有個能安心居住的家，更希望透過我們大家的力量，讓部落的故事能被更多人知道，加速部落籌措500萬永久屋費用的過程。</p>
